		<b>Was ist Alp-Transit-Cargo?</b><br>
		2006 wurde das Projekt Alp-Transit-Cargo lanciert. Es hat zum Ziel,
		Durchfahrtsrechte für die Transitländer zu verkaufen, Fracht beim Zoll
		deklarieren oder andere Anträge direkt über uns bei den Behörden
		abzuwickeln.<br>
		Des weiteren soll mit dem Angebot ein Internet-Service für Umzuege,
		Transporte und leeren Frachtraum einer weltweiten Gemeinschaft und
		eines globalen Marktes geschaffen werden.<br>

		<br>
		Wenn die so erfassten relevanten Informationen in moderne
		Kommunikations-, Analyse- und Informationstechnologien einfliessen
		können stösst dieses Projekt national wie auch international auf
		grosses Interesse. Ende Dezember 2006 fiel nach einer intensiven
		Konzept- und Vorbereitungsphase der Startschuss für die Umsetzung des
		Projektes. Der Realisierungshorizont beträgt insgesamt rund 12 Monate
		in Arbeitstagen. <br>
		<br>
		<b>Wie wird Alp-Transit-Cargo finanziert?</b><br>
		Zur Finanzierung der Umsetzung des Gesamtprojektes wurden rund 1,2 Mio
		CHF veranschlagt. Dieser Betrag wird in erster Linie über die Beiträge
		der vertraglich gebundenen Kooperationspartner gedeckt. Als
		Kooperationspartner kommen ausschliesslich Organisationen auf
		nationaler Ebene, die einen engen Bezug zur Thematik aufweisen, in
		Frage. Es sind dies prioritär die nationalen Verbände der
		Berufsorganisationen sowie die Behörden. <br>
		<br>
		<b>Warum ein Sponsoring?</b><br>
		Trotz dieser Eigenleistungen der Kooperationspartner benötigt das
		Projekt noch zusätzliche Mittel. Davon betroffen ist insbesondere die
		Finanzierung von Experten. Ohne Expertenwissen ist die Gefahr des
		Scheiterns auf Grund von mangelndem Know-how sehr gross. Ein
		Finanzbedarf besteht ebenfalls im Bereich der Kommunikation und
		Information. Neben der eigentlichen Projektarbeit gilt es,
		Alp-Transit-Cargo stets präsent zu halten, über seine Vorhaben und
		seinen aktuellen Stand im Inland wie im Ausland laufend zu informieren
		und den Betroffenen auf allen Ebenen Gelegenheit zu geben, sich mit
		dem Projekt und seinen Auswirkungen kritisch und konstruktiv
		auseinander zu setzen. Dies ist für das Gelingen des Projektvorhabens
		ebenfalls unabdingbar. Auch das beste Projektvorhaben scheitert, wenn
		die Bereitschaft, dessen Ergebnisse umzusetzen, nicht vorhanden ist
		und das Verständnis dafür fehlt. Der Aufwand, welcher hierfür
		betrieben werden muss, ist in jedem Falle signifikant, jedoch ex ante
		kaum zu budgetieren, da flexibel auf Anfragen und Problemstellungen
		reagiert werden muss. <br>
		<br>
		<b>Was bringt ein Sponsoring von Alp-Transit-Cargo?</b><br>
		Immer wieder signalisieren einzelne Institutionen oder Organisationen
		Interesse, aktiv bei der Umsetzung von Alp-Transit-Cargo mitzuwirken.
		Da der Aufbau der Projekt-Organisation es ihnen verunmöglicht, als
		Kooperationspartner aufzutreten, wird ihnen die Möglichkeit einer
		Beteiligung in der Form des Sponsorings geboten. Durch das Sponsoring
		trägt der betroffene Betrieb oder die betroffene Organisation dazu
		bei, dass Alp-Transit-Cargo effizient umgesetzt werden kann. Zudem
		demonstriert er/sie den Willen und die Bereitschaft, sich aktiv für
		die Projektziele einzusetzen und die Idee von Alp-Transit-Cargo zu
		unterstützen. <br>
		<br>
		<b>Wie erfolgt das Sponsoring von Alp-Transit-Cargo?</b><br>
		Das Sponsoring kann auf verschiedene Arten erfolgen.<BR>
		<br>
		=> Finanzierung des Aufwandes eines oder mehrerer Experten<br>
		=> Übernahme der Kosten von Veranstaltungen, Informationstagungen,
		etc.<br>
		=> (Unspezifische) finanzielle Zuwendungen<br>
		=> Zuwendungen in der Form von Naturalleistungen, wie <br>
		=> Übersetzung von Dokumenten in andere Landessprachen<br>
		=> Zur Verfügung-Stellung von Lokalitäten<br>
		=> Bereitstellung von technischen Hilfsmitteln<br>

		<br>

		Der Sponsor erhält eine schriftliche Bestätigung über seinen
		geleisteten Beitrag. Gegebenenfalls und je nach Art und Umfang der
		Gegenleistung kann auch ein Sponsoring-Vertrag abgeschlossen werden. <br>
		<br>
		<b>Wer kann/soll Alp-Transit-Cargo sponsern?</b><br>
		Das Sponsoring steht allen Unternehmen, Institutionen, Organisationen
		oder Personen offen, die die Ziele von Alp-Transit-Cargo unterstützen.
		Dies können Personen oder Organisationen sein, die am Projekt bereits
		ohne vertraglich geregelte finanzielle Beteiligung mitwirken. Das
		Sponsoring soll jedoch auch gewinnorientierten Unternehmen offen
		stehen, die damit eine Stärkung ihres Goodwills und ihrer Visibilität
		verbinden, um ihr Potenzial in einer wachsenden Branche positiv zu
		beeinflussen. <br>
		<br>
		<b>Was darf der Sponsor erwarten (Gegenleistung)?</b><br>
		Ohne anders lautende Mitteilungen wird der Name auf der Website von
		Alp-Transit-Cargo publiziert. Der Sponsor kann auf die Publikation
		schriftlich verzichten. <BR>
		Auf Wunsch kann er Teilhaber/Partner an Alp-Transit-Cargo werden: <br>
		<BR>
		=> Position im Aufsichtsrat.<br>
		=> Inhaltliche oder strukturelle Einflussnahme auf das Projekt.<br>
		<BR>

		Auf Wunsch wird die Publikation um Folgendes ergänzt: <br>
		=> URL auf die Hompage.<br>
		=> Art und Höhe des gesponserten Beitrags.<br>
		<BR>

		Auf Wunsch und je nach Gelegenheit werden Sponsoren aufgeführt: <br>
		=> In öffentlich erhältlichen Print-Publikationen von
		Alp-Transit-Cargo<br>
		=> An Veranstaltungen von Alp-Transit-Cargo<br>
		<br>
		Der Sponsor wird regelmässig über den Stand des Projektes
		Alp-Transit-Cargo informiert. Sofern der gesponserte Beitrag
		zweckgebunden erfolgt ist, hat der Sponsor ein Anrecht auf eine
		entsprechende Rechnungslegung. Übernimmt der Sponsor die Kosten eines
		bestimmten Anlasses (Veranstaltung, Informationstagung etc.), kann ihm
		im Rahmen des Anlasses eine besondere Werbefläche ermöglicht werden. <br>
		<br>
		<b>Was darf der Sponsor nicht erwarten?</b><br>
		Sollten finanzielle Zuwendungen oder Naturalien im Nachhinein explizit
		oder implizit an entsprechende Bedingungen geknüpft werden, wird die
		Partnerschaft mit dem Sponsor mit sofortiger Wirkung aufgelöst. Auf
		gesprochene Mittel und Naturalien besteht keine Rückzahlungs- oder
		Rückgabepflicht. <br>
